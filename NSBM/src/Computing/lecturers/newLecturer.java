    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        CLecturer bl=new CLecturer();
        bl.setLecid(0);
        bl.setFullname(txtFullName.getText());
        bl.setName(txtName.getText());
        bl.setNIC(txtnic.getText());
        bl.setSubject(txtSubjects.getText());
        bl.setTeachingyear(txtTeachingYear.getText());
        bl.setDegree(txtDegree.getText());
        bl.setUniversity(txtUniversity.getText());
        bl.setExperiance(txtExperiance.getText());
        bl.setPhone(txtPhone.getText());
        bl.setEmail(txtEmail.getText());
        bl.setAddress(txtAddress.getText());
        if(bldb.addlecturer(bl)){
            JOptionPane.showMessageDialog(this, "Successfully inserted values!!!");
            clearfields();
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Error occured hile entering values!!!");
            clearfields();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    
