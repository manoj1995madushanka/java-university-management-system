package base;

/**
 *
 * @author USER
 */
public class Lecturer {
    private int lecid;
    private String fullname;
    private String name;
    private String NIC;
    private String subject;
    private String teachingyear;
    private String degree;
    private String university;
    private String experiance;
    private String phone;
    private String email;
    private String address;

    /**
     * @return the lecid
     */
    public int getLecid() {
        return lecid;
    }

    /**
     * @param lecid the lecid to set
     */
    public void setLecid(int lecid) {
        this.lecid = lecid;
    }

    /**
     * @return the fullname
     */
    public String getFullname() {
        return fullname;
    }

    /**
     * @param fullname the fullname to set
     */
    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the NIC
     */
    public String getNIC() {
        return NIC;
    }

    /**
     * @param NIC the NIC to set
     */
    public void setNIC(String NIC) {
        this.NIC = NIC;
    }

    /**
     * @return the subject
     */
    public String getSubject() {
        return subject;
    }

    /**
     * @param subject the subject to set
     */
    public void setSubject(String subject) {
        this.subject = subject;
    }

    /**
     * @return the teachingyear
     */
    public String getTeachingyear() {
        return teachingyear;
    }

    /**
     * @param teachingyear the teachingyear to set
     */
    public void setTeachingyear(String teachingyear) {
        this.teachingyear = teachingyear;
    }

    /**
     * @return the degree
     */
    public String getDegree() {
        return degree;
    }

    /**
     * @param degree the degree to set
     */
    public void setDegree(String degree) {
        this.degree = degree;
    }

    /**
     * @return the university
     */
    public String getUniversity() {
        return university;
    }

    /**
     * @param university the university to set
     */
    public void setUniversity(String university) {
        this.university = university;
    }

    /**
     * @return the experiance
     */
    public String getExperiance() {
        return experiance;
    }

    /**
     * @param experiance the experiance to set
     */
    public void setExperiance(String experiance) {
        this.experiance = experiance;
    }

    /**
     * @return the phone
     */
    public String getPhone() {
        return phone;
    }

    /**
     * @param phone the phone to set
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }
}
